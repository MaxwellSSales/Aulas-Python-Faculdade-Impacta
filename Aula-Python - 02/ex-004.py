# Crie um programa que solicite ao úsuario 3 números inteiros, multiplique-os e exiba o resultado.

n1 = int(input("Digite o 1º número: "))
n2 = int(input("Digite o 2º número: "))
n3 = int(input("Digite o 3º número: "))
mult = n1 * n2 * n3
print(f"O Primeiro número digitado foi {n1} o Segundo foi {n2} o Terceiro foi {n3}\nA multiplicação dos Três é {mult}")
