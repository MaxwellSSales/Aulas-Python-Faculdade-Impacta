#Escreva um programa que permita a entrada de duas notas
#(variáveis reais: float), calcule sua média aritmética e exiba o resultado.

nota1 = float(input("Digite a Primeira Nota: "))
nota2 = float(input("Digite a Segunda Nota: "))
print(f"A Primeira nota foi {nota1} e a Segunda {nota2}\nA media das notas é {(nota1 + nota2) / 2}")